        totalCosts: baseData.material + baseData.mfgExpenses + baseData.belowGP,
        totalCostRatio: baseData.sales > 0 ? ((baseData.material + baseData.mfgExpenses + baseData.belowGP) / baseData.sales) * 100 : 0
