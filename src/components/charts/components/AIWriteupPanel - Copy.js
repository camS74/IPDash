      // Financial Performance Overview
      aiText += `**FINANCIAL PERFORMANCE OVERVIEW**\n\n`;
      
      // Revenue Analysis
      aiText += `**Revenue & Volume Analysis:**\n`;
      aiText += `‚Ä¢ Total Revenue: ${(baseData.sales/1000000).toFixed(2)}M\n`;
      aiText += `‚Ä¢ Sales Volume: ${(baseData.salesVolume/1000).toFixed(1)} MT\n`;
      aiText += `‚Ä¢ Average Selling Price: ${metrics.pricePerKg.toFixed(3)}/kg\n`;
      aiText += `‚Ä¢ Revenue per Ton: ${(metrics.revenuePerTon/1000).toFixed(0)}k\n`;
      
      // Only include market positioning if not excluded
      if (!userPreferences.excludeMarketPositioning) {
        aiText += `‚Ä¢ Market positioning: ${metrics.pricePerKg > 3.0 ? 'Premium pricing strategy' : metrics.pricePerKg > 2.0 ? 'Competitive market positioning' : 'Value-focused pricing approach'}\n`;
      }
      aiText += `\n`;
      
      // Profitability Analysis (enhanced if user prefers margin focus)
      aiText += `**Profitability Analysis:**\n`;
      aiText += `‚Ä¢ Gross Profit: ${(baseData.grossProfit/1000000).toFixed(2)}M (${metrics.grossMargin.toFixed(1)}% margin)\n`;
      aiText += `‚Ä¢ EBITDA: ${(baseData.ebitda/1000000).toFixed(2)}M (${metrics.ebitdaMargin.toFixed(1)}% margin)\n`;
      aiText += `‚Ä¢ Net Profit: ${(baseData.netProfit/1000000).toFixed(2)}M (${metrics.netMargin.toFixed(1)}% margin)\n`;
